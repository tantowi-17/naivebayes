          <h3 align="center">Algoritma Naive Bayes</h3>
          <p>
          Naive Bayes adalah suatu metode klasifikasi yang berdasarkan pada teorema Bayes dan asumsi independensi antar fitur. Metode ini sering digunakan untuk membuat model klasifikasi dalam berbagai aplikasi, seperti pengklasifikasian teks, filter email spam, dan analisis sentimen.
          </p>
          <p>
          Teorema Bayes membantu untuk menghitung probabilitas suatu kelas diberikan beberapa fitur. Asumsi independensi antar fitur berarti bahwa pengaruh satu fitur terhadap kelas tidak dipengaruhi oleh fitur lain. Dengan demikian, setiap fitur dapat dianalisis secara terpisah dan kemudian digabungkan untuk membuat keputusan akhir tentang kelas yang paling mungkin.
          </p>
          <p>
          Kelebihan dari metode Naive Bayes adalah kecepatan komputasinya yang cepat dan efisiensi, serta kemudahannya dalam memproses data berskala besar. Namun, asumsi independensi antar fitur yang tidak selalu benar membuat metode ini kurang akurat dibandingkan metode lain seperti Random Forest atau SVM.
          </p>
